public class OperadoresIncremento {
    
    public static void main(String[] args) {
        
        int numero = 5;

        //Repetição
        numero ++;
        // Isso é igual numero = numero + 1;

        System.out.println(numero);


        boolean variavel = true;

        variavel = !variavel;
        
        System.out.println(variavel);
        
    }

}
